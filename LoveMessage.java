public class LoveMessage {
    public static void main(String[] args) {
        String message = "Para o amor da minha vida,"; // "To the love of my life,"
        String heart = "<3";

        // Exibindo a mensagem romântica
        System.out.println(message);
        System.out.println("Toda linha de código que eu escrevo, eu penso em você."); // "Every line of code I write, I think of you."
        System.out.println("Assim como este programa, você completa a minha vida."); // "Just like this program, you complete my life."
        System.out.println("Você é a razão pela qual meu coração compila com alegria " + heart); // "You are the reason my heart compiles with joy"
        System.out.println("E eu estou sempre em um loop infinito de amor por você."); // "And I'm always in an infinite loop of love with you."

        // Pequeno loop para demonstrar carinho infinito
        for (int i = 0; i < 3; i++) {
            System.out.println("Eu te amo " + heart); // "I love you"
        }

        System.out.println("Para sempre sua."); // "Forever yours."
    }
}
